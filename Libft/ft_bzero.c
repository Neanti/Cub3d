#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	i = 0;
	if (n <= 0)
		return ;
	else
	{
		tmp = b;
		while (i < n)
		{
			*tmp = 0;
			tmp++;
			i++;
		}
		return ;
	}
}
