#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*out;

	i = 0;
	while (str[i])
		i++;
	if ((out = (char*)malloc(sizeof(char) * i + 1)) == NULL)
		return (NULL);
	i = 0;
	while (str[i])
	{
		out[i] = str[i];
		i++;
	}
	out[i] = '\0';
	return (out);
}
